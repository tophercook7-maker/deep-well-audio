/**
 * Generates SQL INSERT for public.bible_books from canonical order + chapter counts.
 * Run: node scripts/gen-bible-books-seed.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const chapterSrc = readFileSync(join(root, "lib/bible/chapter-counts.ts"), "utf8");
const booksSrc = readFileSync(join(root, "lib/study/bible-books.ts"), "utf8");

const counts = {};
for (const line of chapterSrc.split("\n")) {
  const m = /^\s*(?:"([A-Z0-9]+)"|([A-Z]+)):\s*(\d+),?\s*$/.exec(line);
  if (!m) continue;
  const id = m[1] || m[2];
  counts[id] = Number(m[3]);
}

const books = [];
const defRe = /def\("([^"]+)",\s*"([^"]+)",\s*"([A-Z0-9]+)"/g;
let dm;
while ((dm = defRe.exec(booksSrc))) {
  books.push({ label: dm[1], apiSlug: dm[2], apiId: dm[3] });
}

const canon = [
  "GEN","EXO","LEV","NUM","DEU","JOS","JDG","RUT","1SA","2SA","1KI","2KI","1CH","2CH","EZR","NEH","EST","JOB","PSA","PRO","ECC","SNG","ISA","JER","LAM","EZK","DAN","HOS","JOL","AMO","OBA","JON","MIC","NAH","HAB","ZEP","HAG","ZEC","MAL","MAT","MRK","LUK","JHN","ACT","ROM","1CO","2CO","GAL","EPH","PHP","COL","1TH","2TH","1TI","2TI","TIT","PHM","HEB","JAS","1PE","2PE","1JN","2JN","3JN","JUD","REV",
];

const byId = Object.fromEntries(books.map((b) => [b.apiId, b]));
let sort = 0;
const rows = [];
for (const id of canon) {
  const b = byId[id];
  if (!b) throw new Error("Missing book " + id);
  sort += 1;
  const slug = b.apiSlug.replace(/\+/g, "-");
  const ch = counts[id];
  if (!ch) throw new Error("Missing chapters for " + id);
  const testament = sort < 40 ? "ot" : "nt";
  const label = b.label.replace(/'/g, "''");
  rows.push(`('${slug}','${label}','${id}', '${testament}', ${sort}, ${ch})`);
}

const sql = `-- auto-generated by scripts/gen-bible-books-seed.mjs
INSERT INTO public.bible_books (slug, name, api_book_id, testament, sort_order, chapter_count) VALUES
${rows.join(",\n")}
ON CONFLICT (slug) DO NOTHING;
`;

const out = join(root, "supabase/migrations/20260418120100_seed_bible_books.sql");
writeFileSync(out, sql);
console.log("Wrote", out);
